    private void txtnameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnameKeyReleased

        Pattern pNum=Pattern.compile("[A-Za-z]{3,9}");
        Matcher mNam=pNum.matcher(txtname.getText());
        Pattern pEmail=Pattern.compile("[a-zA-Z0-9_\\-\\.]+[@]+[a-z]+[\\.]+[a-z]{2,3}");
        Matcher mEmail=pEmail.matcher(txtname.getText());
        if(mNam.find()||mEmail.find()){
            nam.setText("");
        }
        else{
            nam.setText("Wrong!");
        }
    }//GEN-LAST:event_txtnameKeyReleased

